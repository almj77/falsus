namespace Falsus.Shared.Avatar.Enums
{
    /// <summary>
    /// Specifies the Avatar eyebrow type.
    /// </summary>
    /// <remarks>
    /// For more details see Avataar documentation https://getavataaars.com/.
    /// </remarks>
    public enum AvatarEyebrowType
    {
        /// <summary>
        /// Angry eyebrows.
        /// </summary>
        Angry,

        /// <summary>
        /// Angry (thicker, less raised) eyebrows.
        /// </summary>
        AngryNatural,

        /// <summary>
        /// Default eyebrows.
        /// </summary>
        Default,

        /// <summary>
        /// Default (thicker, semi-circle like) eyebrows.
        /// </summary>
        DefaultNatural,

        /// <summary>
        /// Flat eyebrows.
        /// </summary>
        FlatNatural,

        /// <summary>
        /// Excited eyebrows.
        /// </summary>
        RaisedExcited,

        /// <summary>
        /// Excited (thicker, semi-circle like) eyebrows.
        /// </summary>
        RaisedExcitedNatural,

        /// <summary>
        /// Sad/Concerned eyebrows.
        /// </summary>
        SadConcerned,

        /// <summary>
        /// Sad/Concerned (thicker, semi-circle like) eyebrows.
        /// </summary>
        SadConcernedNatural,

        /// <summary>
        /// Unibrows.
        /// </summary>
        UnibrowNatural,

        /// <summary>
        /// One raised eyebrow.
        /// </summary>
        UpDown,

        /// <summary>
        /// One raised (thicker, semi-circle like) eyebrow.
        /// </summary>
        UpDownNatural
    }
}
